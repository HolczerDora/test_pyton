import turtle

# Window

window = turtle.Screen()
window.setup(width=900,height=900)
window.bgcolor("purple")
window.title("Pingi-Pongi")
window.tracer()

# Turtle way
dorka = turtle.Turtle()
dorka.speed(2)
dorka.shape("turtle")
dorka.color("white")
dorka.forward(200)
dorka.right(90)
dorka.forward(200)
dorka.right(90)
dorka.forward(200)
dorka.right(90)
dorka.forward(200)
dorka.right(90)


